    private void carregarFilmesAlocados() {
    try {
        filmeDAO dao = new filmeDAO();
        List<filme> lista = dao.listarPorStatus("locado"); 
        
        usuarioDAO uDao = new usuarioDAO();
        
        usuario user = uDao.buscarPorLogin(loginatual);
        
        String[][] dados = new String[lista.size()][2];
        for (int i = 0; i < lista.size(); i++) {
            filme f = lista.get(i);
            dados[i][0] = String.valueOf(f.id_filme); 
            dados[i][1] = f.titulo;
        }

        String[] colunas = {"ID", "Título"};
        tabelaDevolver.setModel(new javax.swing.table.DefaultTableModel(dados, colunas));
        
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Erro ao carregar lista: " + e.getMessage());
    }
}
    
