    private void devolverbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_devolverbtnActionPerformed
        // TODO add your handling code here:
        try {
        int idFilme = Integer.parseInt(idTxt.getText());

        filme f = new filme();
        f.id_filme = idFilme;
        f.status = "disponivel"; 

        filmeDAO dao = new filmeDAO();
        dao.atualizar(f);

        JOptionPane.showMessageDialog(this, "Filme devolvido com sucesso!");
        
        idTxt.setText("");
        carregarFilmesAlocados();

    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Selecione um filme válido na tabela.");
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Erro ao devolver: " + e.getMessage());
    }
    }//GEN-LAST:event_devolverbtnActionPerformed

