    private void deletarbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deletarbtnActionPerformed
        // TODO add your handling code here:

   int linhaSelecionada = tabeladeletar.getSelectedRow();

    if (linhaSelecionada == -1) {
        JOptionPane.showMessageDialog(this, "Por favor, selecione um filme na tabela primeiro.");
        return;
    }
    try {
        String idString = tabeladeletar.getValueAt(linhaSelecionada, 0).toString();
        int idParaDeletar = Integer.parseInt(idString);
        String nomeFilme = tabeladeletar.getValueAt(linhaSelecionada, 1).toString();
        int confirmacao = JOptionPane.showConfirmDialog(this, 
                "Tem certeza que deseja excluir o filme: " + nomeFilme + "?", 
                "Confirmar Exclusão", 
                JOptionPane.YES_NO_OPTION);
        if (confirmacao == JOptionPane.YES_OPTION) {
            filmeDAO dao = new filmeDAO();
            dao.deletar(idParaDeletar); 
            
            JOptionPane.showMessageDialog(this, "Filme excluído com sucesso!");
            carregarTabelaDeletar(); 
        }
        
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Erro ao processar exclusão: " + e.getMessage());
        e.printStackTrace();
    }
    }//GEN-LAST:event_deletarbtnActionPerformed

