    private void carregarTabelaDeletar() {
    try {
        filmeDAO dao = new filmeDAO();
        List<filme> lista = dao.listar(); 
        
        String[][] dados = new String[lista.size()][3];

        for (int i = 0; i < lista.size(); i++) {
            filme f = lista.get(i);
            dados[i][0] = String.valueOf(f.getId_filme());
            dados[i][1] = f.getTitulo();
            dados[i][2] = f.getStatus();
        }

        String[] colunas = {"ID", "Título", "Status"};
        tabeladeletar.setModel(new javax.swing.table.DefaultTableModel(dados, colunas));
        
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Erro ao carregar tabela: " + e.getMessage());
    }
}
    
