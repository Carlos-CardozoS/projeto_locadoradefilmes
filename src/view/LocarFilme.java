    private void carregarFilmesDisponiveis() {
    try {
        filmeDAO dao = new filmeDAO();
        List<filme> lista = dao.listarPorStatus("disponivel"); 
        
        String[][] dados = new String[lista.size()][2];
        for (int i = 0; i < lista.size(); i++) {
            filme f = lista.get(i);
            dados[i][0] = String.valueOf(f.getId_filme());
            dados[i][1] = f.getTitulo();
        }

        String[] colunas = {"ID", "Título"};
        tabelaAlocar.setModel(new javax.swing.table.DefaultTableModel(dados, colunas));
        
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Erro ao carregar: " + e.getMessage());
    }
}
    
    
