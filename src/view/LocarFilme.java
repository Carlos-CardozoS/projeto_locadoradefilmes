    private void locarbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_locarbtnActionPerformed
        // TODO add your handling code here:
        try {
        int idFilme = Integer.parseInt(idTxt.getText()); 
        
        String nomeCliente = JOptionPane.showInputDialog(this, "Nome do Cliente:");

        if (nomeCliente != null && !nomeCliente.trim().isEmpty()) {
            filmeDAO dao = new filmeDAO();
            
            boolean sucesso = dao.alocacao(idFilme, nomeCliente);

            if (sucesso) {
                JOptionPane.showMessageDialog(this, "A locação foi feita com sucesso!");
                idTxt.setText("");
                carregarFilmesDisponiveis();
            } else {
                JOptionPane.showMessageDialog(this, "Erro ao efetuar a locação.");
            }
        }
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Selecione um filme válido.");
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Erro: " + e.getMessage());
    }
    }//GEN-LAST:event_locarbtnActionPerformed

