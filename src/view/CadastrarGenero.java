    private void cadastrarbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cadastrarbtnActionPerformed

    String nomeGenero = generoTxt.getText();

    
    if (nomeGenero.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Por favor, digite o nome do gênero.", "ERRO", JOptionPane.ERROR_MESSAGE);
    } else {
        try {
            filmeDAO fDao = new filmeDAO(); 

            model.genero novoGenero = new model.genero();
            
            novoGenero.nome = nomeGenero; 

            fDao.cadastrargen(novoGenero); 

            JOptionPane.showMessageDialog(this, "Gênero cadastrado com sucesso!", "SUCESSO", JOptionPane.INFORMATION_MESSAGE);
            
            CarregarTabela();
            
            generoTxt.setText("");
            generoTxt.requestFocus();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao cadastrar: " + e.getMessage(), "ERRO", JOptionPane.ERROR_MESSAGE);
        }
    }
    }//GEN-LAST:event_cadastrarbtnActionPerformed

    private void deletarbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deletarbtnActionPerformed
        // TODO add your handling code here:
        int linhaSelecionada = tabelagen.getSelectedRow();

    if (linhaSelecionada == -1) {
        JOptionPane.showMessageDialog(this, "Por favor, selecione um gênero na tabela para deletar.", "Aviso", JOptionPane.WARNING_MESSAGE);
    } else {
        int confirmacao = JOptionPane.showConfirmDialog(this, "Tem certeza que deseja excluir este gênero?", "Confirmação", JOptionPane.YES_NO_OPTION);
        
        if (confirmacao == JOptionPane.YES_OPTION) {
            try {
                int id = Integer.parseInt(tabelagen.getValueAt(linhaSelecionada, 0).toString());

                filmeDAO fDao = new filmeDAO();
                fDao.deletarGenero(id); 

                JOptionPane.showMessageDialog(this, "Gênero excluído com sucesso!");

                CarregarTabela(); 
                generoTxt.setText("");

            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Erro ao deletar: " + e.getMessage(), "ERRO", JOptionPane.ERROR_MESSAGE);
            }
        }
    }
    }//GEN-LAST:event_deletarbtnActionPerformed

