private void CarregarTabela() {
    try {
        filmeDAO dao = new filmeDAO();
        List<model.genero> listaGeneros = dao.listarGenerosLimpos(); 
        
        String[][] dados = new String[listaGeneros.size()][2];
        
        for (int i = 0; i < listaGeneros.size(); i++) {
            dados[i][0] = String.valueOf(listaGeneros.get(i).getId());
            dados[i][1] = listaGeneros.get(i).nome;
        }
        
        String[] colunas = {"ID", "Gênero"};
        tabelagen.setModel(new javax.swing.table.DefaultTableModel(dados, colunas));
        
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Erro ao carregar tabela: " + e.getMessage());
    }
    }
     

    
