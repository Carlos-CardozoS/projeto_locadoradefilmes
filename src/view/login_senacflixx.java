    private void btnLogarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLogarActionPerformed

String usuarioDigitou = usuarioTxt.getText();
String senhaDigitada = new String(senhaTxt.getPassword());
if(usuarioDigitou.isEmpty() || senhaDigitada.isEmpty()){
    JOptionPane.showMessageDialog(this, "Preencha ambos os campos (Usuario e Senha)");
    return;
}

String senhaCriptografada = seguranca.gerarHash(senhaDigitada);

try{
    usuarioDAO lDao = new usuarioDAO();
    
    if(lDao.autenticacao(usuarioDigitou, senhaCriptografada)){
        JOptionPane.showMessageDialog(null, "Login efetuado com sucesso!");
        MenuPrincipal mp = new MenuPrincipal();
        mp.setVisible(true);
        dispose();
    }else{
        JOptionPane.showMessageDialog(null, "Usuario ou senha incorretos", "Erro de autenticação", JOptionPane.ERROR_MESSAGE);
        usuarioTxt.setText("");
        senhaTxt.setText("");
        usuarioTxt.requestFocus();
    }
}       catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Erro de conexão com o banco de dados.");
    }//GEN-LAST:event_btnLogarActionPerformed
    }
