    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        // TODO add your handling code here:
        String usuarioDigitou = usuarioTxt.getText();
String senhaDigitada = new String(senhaTxt.getPassword());
if(usuarioDigitou.isEmpty() || senhaDigitada.isEmpty()){
    JOptionPane.showMessageDialog(this, "Preencha um usuario e senha para criar o cadastro");
    return;
}

String senhaCriptografada = seguranca.gerarHash(senhaDigitada);

try{
    usuarioDAO lDao = new usuarioDAO();
    
    if(lDao.criarLogin(usuarioDigitou, senhaCriptografada)){
        JOptionPane.showMessageDialog(this, "Login criado com sucesso! agora você pode logar");
        
    }else{
        JOptionPane.showMessageDialog(this,"Erro ao criar Login. O usuário já pode existir.");
        
    }
   }  catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Erro de conexão com o banco de dados.");
        }
    }                                        
